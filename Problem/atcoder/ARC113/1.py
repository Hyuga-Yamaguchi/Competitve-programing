def main():
    k = int_input()
    def prime_factorization(n):
        a = []
        while n % 2 == 0:
            a.append(2)
            n //= 2
        f = 3
        while f * f <= n:
            if n % f == 0:
                a.append(f)
                n //= f
            else:
                f += 2
        if n != 1:
            a.append(n)
        return a

    ans = 0
    for i in range(1, k + 1):
        c = Counter(prime_factorization(i))
        lis = c.values()
        #print(lis)
        tmp = 1
        for j in lis:
            tmp *= (j + 2) * (j + 1) // 2
        ans += tmp
    print(ans)

